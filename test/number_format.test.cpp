struct test_result
{
    int passed = 0;
    int failed = 0;
};

void test_number_format()
{
    test_result total;

    auto to_string = [](double number)
    {
        char buf[64]{};
        std::to_chars(buf, buf + sizeof(buf), number);
        return std::string(buf);
    };

    std::cout << "=== number_format ECMA-376 Compliance Tests ===" << std::endl
              << std::endl;

    for (const auto &tc : test_cases)
    {
        try
        {
            xlsxtext::number_format fmt(tc.format_code);
            std::string result = tc.is_number() ? fmt.format(tc.number()) : fmt.format(tc.text());
            bool pass = (result == tc.expected);
            if (pass)
            {
                total.passed++;
            }
            else
            {
                total.failed++;
                std::cout << "[FAIL] format: \"" << tc.format_code << "\""
                          << ", value: " << (tc.is_number() ? to_string(tc.number()) : "\"" + tc.text() + "\"")
                          << ", result: \"" << result << "\""
                          << ", expected: \"" << tc.expected << "\""
                          << std::endl;
            }
        }
        catch (const std::exception &e)
        {
            total.failed++;
            std::cout << "[ERROR] format: \"" << tc.format_code << "\" - " << e.what() << std::endl;
        }
    }

    std::cout << std::endl;
    std::cout << "=== Test Summary ===" << std::endl;
    std::cout << "Passed: " << total.passed << std::endl;
    std::cout << "Failed: " << total.failed << std::endl;
    std::cout << "Total:  " << (total.passed + total.failed) << std::endl;

    if (total.failed == 0)
    {
        std::cout << "\n*** ALL TESTS PASSED ***" << std::endl;
    }
    else
    {
        std::cout << "\n*** " << total.failed << " TEST(S) FAILED ***" << std::endl;
    }
}

int main()
{
#ifdef _WIN32
    auto __con_out_cp = GetConsoleOutputCP();
    SetConsoleOutputCP(CP_UTF8);
#endif

    test_number_format();

#ifdef _WIN32
    SetConsoleOutputCP(__con_out_cp);
#endif
    return 0;
}